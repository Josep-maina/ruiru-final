<!-- History Section -->
<section class="py-5 bg-light">
    <div class="container">
        <div class="row">
            <div class="col-lg-8 mx-auto">
                <h2 class="section-title text-center mb-5">Our History</h2>
                <div class="history-content">
                    <p class="lead mb-4">Ruiru Technical and Vocational College (RTVC) has a brief but impactful history.</p>
                    
                    <p class="mb-4">The college was established to meet the growing demand for technical education in the Ruiru area. It opened its doors in <strong>2023</strong>, built on a generous <strong>7-acre piece of land</strong> donated by the local community. The construction was funded by the National Government Constituencies Development Fund (NG-CDF), which allocated <strong>Ksh 10 million</strong> for this project.</p>
                    
                    <p class="mb-4">Initially, RTVC was managed and mentored by <strong>Kabete National Polytechnic</strong>, which helped set up the institution and ensure it met educational standards. The college was designed to accommodate around <strong>1,000 students</strong>, offering various technical and vocational courses, with the aim of equipping youths with knowledge, practical skills and market driven competencies for self-reliance: <em>"Ujuzi wa kujitegemea."</em></p>
                    
                    <p class="mb-4">In <strong>2024</strong>, a significant milestone was reached when the college received its own local management board. This change allowed RTVC to operate more independently and focus on tailoring its programs to better serve the needs of the community and its students.</p>
                    
                    <p class="mb-0">Today, Ruiru Technical and Vocational College continues to play a vital role in empowering youths through education and skill development, contributing to the local economy and workforce.</p>
                </div>
            </div>
        </div>
    </div>
</section>

